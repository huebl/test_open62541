int main(void) {
    signal(SIGINT, stopHandler);
    signal(SIGTERM, stopHandler);

    UA_Server *server = UA_Server_new();
    UA_ServerConfig_setDefault(UA_Server_getConfig(server));

    /* Add all valid capabilities */
    size_t i = 0;
    for (i = 0; i < validServerConfigCapabilitiesCount; i++) {
    	UA_String str;
    	UA_String_init(&str);
        str = UA_String_fromChars(validServerConfigCapabilities[i]);
        UA_Server_configAddCapability(server, &str);
        UA_String_clear(&str);
    }

    /* Add all valid keyFormats */
    for (i = 0; i < validServerConfigKeyFormatsCount; i++) {
    	UA_String str;
        UA_String_init(&str);
        str = UA_String_fromChars(validServerConfigKeyFormats[i]);
        UA_Server_configAddKeyFormat(server, &str);
        UA_String_clear(&str);
    }

    /* Set max trust list size variable */
    UA_Server_configSetMaxTrustListSize(server, 7111);

	/* Set the certificate and private key data from encryption/certificates.h */
	UA_ByteString certificate = UA_BYTESTRING_NULL;
	UA_ByteString privateKey = UA_BYTESTRING_NULL;
	certificate.length = CERT_DER_LENGTH;
	certificate.data = CERT_DER_DATA;
	privateKey.length = KEY_DER_LENGTH;
	privateKey.data = KEY_DER_DATA;

	/* Setup Certificate Manager with valid arguments */
    UA_ServerConfig_setupCertificateManager(server, &certificate, &privateKey);

    /* Run server */
    UA_StatusCode retval = UA_Server_run(server, &running);

    UA_Server_delete(server);
    return retval == UA_STATUSCODE_GOOD ? EXIT_SUCCESS : EXIT_FAILURE;
}

